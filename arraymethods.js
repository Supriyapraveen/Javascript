let fruits= ['apple', 'Banana','Orange'];
console.log(fruits);

fruits.pop();
console.log(fruits);

// Remove the first element
fruits.shift();
console.log(fruits);

fruits.push('Apple');
console.log(fruits);

fruits.unshift('Orange');
console.log(fruits);

// delete fruit at the index position 1 

delete fruits[1];
console.log(fruits);

